 S LODT=STDT-.9,ENDT=ENDT+.3,(NUMER,DENOM)=0
 I '$G(STDT)!'$G(ENDT) Q "-1^Invalid Input!"
 F II=1:1  S TABLE=$P($T(TABLES1+II),";",3) Q:TABLE=""  D LOADTBL(TABLE)
 D @("VTE"_RPT)
 K ^TMP("VFD-ARRA",$J)
