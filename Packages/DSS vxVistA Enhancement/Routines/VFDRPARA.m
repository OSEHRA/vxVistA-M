 .F  S MOV=$O(^DGPM("CA",X,MOV)) Q:'MOV  S LOC=+$P(^DGPM(MOV,0),U,6) I LOC,'$D(^TMP("VFD-ICU",$J,LOC)) S X1=$P(^DGPM(MOV,0),U),X2=$P(^DGPM(Q,0),U) D ^%DTC I X<2 S Q=0
