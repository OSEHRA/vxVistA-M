 I 'CODE S EXCL=1 Q
 ;Check for warfarin meds
 S CODE=0 F  S CODE=$O(^TMP("VFD-ARRA",$J,"WARMEDS",CODE)) Q:'CODE  Q:$D(^TMP("VFD-RXS",$J,CODE))
 I 'CODE S EXCL=1 Q  ;No warf meds don't count
 S DENOM=DENOM+1
