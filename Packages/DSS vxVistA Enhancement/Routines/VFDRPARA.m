 ..S ADMDT=$P(^DGPM(ADMMOV,0),U),DCHDT=$S(DCHMOV:$P(^DGPM(DCHMOV,0),U),1:DT)
 ..S X1=DCHDT,X2=ADMDT D ^%DTC S LOS=X
 ..S X1=ADMDT,X2=1 D C^%DTC S ADMPL1=X
