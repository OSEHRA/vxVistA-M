 ;Check for Stroke Diagnosis or VTE
 S CODE=0 F  S CODE=$O(^TMP("VFD-PROC",$J,CODE)) Q:'CODE  I $D(^TMP("VFD-ARRA",$J,"STK",CODE)) S Q=1 Q
 I Q S EXCL=1 Q
