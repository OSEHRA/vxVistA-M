 F  S LODT=$O(^AUPNVSIT("B",LODT)) Q:'LODT!(LODT>ENDT)  D  ;
 .K VADM
 .S VIEN=0 F  S VIEN=$O(^AUPNVSIT("B",LODT,VIEN)) Q:'VIEN  D  ;
 ..D CLNTMP
 ..Q:'$D(^DGPM("AVISIT",VIEN))
 ..S DFN=$$GET1^DIQ(9000010,VIEN,.05,"I"),PRIPRV=$$PRIPRV(VIEN)
