 N RX,RXDR,RXDT,DRUG,RXNORM
 S RX=0 F  S RX=$O(^PS(55,DFN,"P",RX)) Q:'RX  S RXDT=$P(^PSRX(RX,0),U,13) D:RXDT=DCHDT  ;
 .S DRUG=$P(^PSRX(RX,0),U,6),RXNORM=$$RXNORM(DRUG,5),^TMP("VFD-RXS",$J,RXNORM,RX)=""
