 I CODE S NUMER=NUMER+1 Q
 S CODE=0 F  S CODE=$O(^TMP("VFD-DRUG",$J,CODE)) Q:'CODE  Q:$D(^TMP("VFD-ARRA",$J,"MEDS",CODE))
 I CODE S NUMER=NUMER+1 Q
