 ..S X1=DCHDT,X2=ADMDT D ^%DTC S LOS=X
 ..S EXCL=0 D DENOM5,NUMER5:'EXCL
 S AXY=NUMER_U_DENOM_U_(NUMER/DENOM)
 Q
