 N CODE,CD
 F CD=1:1:4 S CODE=0 F  S CODE=$O(^TMP("VFD-ARRA",$J,"EDU"_CD,CODE)) Q:'CODE  I $D(^TMP("VFD-PROC",$J,CODE)) S $P(EDU,U,CD)=1 Q
 Q:EDU'="1^1^1^1"
 S NUMER=NUMER+1
 Q
 ;
